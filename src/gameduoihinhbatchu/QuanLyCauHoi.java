    public void xulyLuu() {
        if (idCauHoi.isEmpty()) {
            return;
        }

        String anh = tfAnh.getText();
        String dapAn = tfDapAn.getText();

        if (anh.isEmpty() || dapAn.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Dữ liệu không được để trống!");
            return;
        }

        String sqlSua = String.format("UPDATE CauHoi SET Anh = '%s', DapAn = '%s' WHERE Id = '%s'", anh, dapAn, idCauHoi);
        int kq = MySql.excuteQuery(sqlSua);
        if (kq != 0) {
            idCauHoi = "";
            tfAnh.setText("");
            tfDapAn.setText("");

            //lay ds cau hoi
            layDanhSachCauHoi();
            
            lbAnh.setVisible(false);
            
             //set btn
            btnThem.setEnabled(true);
            btnLuu.setEnabled(false);
            btnHuy.setEnabled(false);
            btnXoa.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Sửa thất bại");
        }
    }

    public void xuLyXoa() {
        if (idCauHoi.isEmpty()) {
            return;
        }

        String sqlXoa = String.format("DELETE FROM CauHoi WHERE Id = '%s'", idCauHoi);

        int kq = MySql.excuteQuery(sqlXoa);
        if (kq != 0) {
            tfAnh.setText("");
            tfDapAn.setText("");
            idCauHoi = "";

            //set btn
            btnThem.setEnabled(true);
            btnLuu.setEnabled(false);
            btnHuy.setEnabled(false);
            btnXoa.setEnabled(false);

            //lay ds cau hoi
            layDanhSachCauHoi();
            
            lbAnh.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }

    }

    public void xuLyThem() {
        String anh = tfAnh.getText();
        String dapAn = tfDapAn.getText();

        if (anh.isEmpty() || dapAn.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Phải nhập đủ thông tin!");
            return;
        }

        String id = Utils.randomId();
        String sqlThem = String.format("InSERT INTO CauHoi(Id,Anh,DapAn) VALUES ('%s','%s','%s')", id, anh, dapAn);

        int kq = MySql.excuteQuery(sqlThem);
        if (kq != 0) {
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            tfAnh.setText("");
            tfDapAn.setText("");

            //lay ds cau hoi
            layDanhSachCauHoi();
        } else {
            JOptionPane.showMessageDialog(null, "Thêm thất bại");
        }

    }

