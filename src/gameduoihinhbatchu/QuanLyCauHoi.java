    public void layDanhSachCauHoi() {

        try {
            model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Ảnh");
            model.addColumn("Đáp án");
            jTable1.setModel(model);

            String sqlLayCauHoi = "SELECT * FROM CauHoi";
            ResultSet kq = MySql.queryData(sqlLayCauHoi);

            while (kq != null && kq.next()) {
                Object[] data1 = {kq.getString("Id"), kq.getString("Anh"), kq.getString("DapAn")};
                model.addRow(data1);
            }

        } catch (SQLException ex) {
            Logger.getLogger(QuanLyCauHoi.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void setEventTable() {
        ListSelectionModel selectionModel = jTable1.getSelectionModel();
        selectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    // Xử lý sự kiện khi lựa chọn dòng
                    int selectedRow = jTable1.getSelectedRow();
                    if (selectedRow != -1) {
                        // Lấy dữ liệu từ dòng đã chọn
                        idCauHoi = (String) model.getValueAt(selectedRow, 0);
                        String anh = (String) model.getValueAt(selectedRow, 1);
                        String dapAn = (String) model.getValueAt(selectedRow, 2);

                        // Hiển thị thông tin
                        tfAnh.setText(anh);
                        tfDapAn.setText(dapAn);

                        //set button
                        btnThem.setEnabled(false);
                        btnLuu.setEnabled(true);
                        btnHuy.setEnabled(true);
                        btnXoa.setEnabled(true);

                        //gán ảnh
                        new Thread(() -> {
                            try {
                                ImageIcon anhIcon = Utils.layImageIcon(anh, 70, 70);
                                lbAnh.setIcon(anhIcon);
                                lbAnh.setVisible(true);
                            } catch (Exception ex) {
                                System.out.println("URL Không hợp lệ");
                            }

                        }).start();

                    }
                }
            }
        });
    }

