    public void layCauHoi() {

        try {
            lbAnh.setText("Đang tải...");
            lbAnh.setIcon(null);
            String sql = "SELECT * FROM CauHoi ";
            if (!listIdCauHoi.isEmpty()) {
                String listId = "WHERE Id NOT IN (";
                int size = listIdCauHoi.size();
                for (int i = 0; i < size; i++) {
                    if (i != 0) {
                        listId += ", ";
                    }
                    listId += "'" + listIdCauHoi.get(i) + "'";
                }
                listId += ")";

                sql += listId;
            }

            sql += "ORDER BY RAND() LIMIT 1";

            ResultSet kq = MySql.queryData(sql);
            while (kq != null && kq.next()) {
                String id = kq.getString("Id");
                String anh = kq.getString("Anh");
                DapAn = kq.getString("DapAn");

                new Thread(() -> {
                    ImageIcon anhIcon = Utils.layImageIcon(anh, 100, 100);
                    lbAnh.setIcon(anhIcon);
                    lbAnh.setText("");
                }).start();

                listIdCauHoi.add(id);
                return;
            }

            int xacnhan = JOptionPane.showConfirmDialog(null, "Đã hết câu hỏi, điểm tối đa của bạn là: " + diem + "\nBạn có muốn chơi lại không?", "Xin chúc mừng",
                    JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (xacnhan == JOptionPane.OK_OPTION) {
                xuLyChoiLai();
            } else {
                Main.rootFrame.setVisible(true);
                dispose();
            }

        } catch (SQLException ex) {
            Logger.getLogger(TrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

