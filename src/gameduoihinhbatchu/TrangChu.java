    public void xuLyOK() {
        String strDapAn = tfDapAn.getText();
        if (strDapAn.isEmpty()) {
            return;
        }

        if (DapAn.equals(strDapAn)) {

            diem++;

            lbDiem.setText("Điểm: " + diem);

            tfDapAn.setText("");
            layCauHoi();

        } else {
            JOptionPane.showMessageDialog(null, "Đáp án không chính xác!");
        }
    }

